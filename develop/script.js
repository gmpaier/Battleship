function postMove () {
  let coordData = $(this).attr("value")
  let coord = JSON.parse(coordData);
  let row = coord[0];
  let col = coord[1];
  if (activeBoard[row][col][0] === 1){
    activeBoard[row][col][1] = 1;
    $('#result-text').text("Hit at " + coordData);
  }
  else {
    activeBoard[row][col][1] = 1;
    $('#result-text').text("Miss at " + coordData);
  }
}


$(document).on("click", ".grid-item", postMove);